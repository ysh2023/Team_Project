package board.model;

public class RecommendBean {
	private int recNum;
	private int bodNum;
	private String id;

	public int getRecNum() {
		return recNum;
	}

	public void setRecNum(int recNum) {
		this.recNum = recNum;
	}

	public int getBodNum() {
		return bodNum;
	}

	public void setBodNum(int bodNum) {
		this.bodNum = bodNum;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

}
