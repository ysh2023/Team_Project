package board.model;

public class BoardIngredientBean {
	private int bigNum;
	private String ingNum;
	private int bodNum;
	private String bigName;
	private String bigAmount;

	public int getBigNum() {
		return bigNum;
	}

	public void setBigNum(int bigNum) {
		this.bigNum = bigNum;
	}

	public String getIngNum() {
		return ingNum;
	}

	public void setIngNum(String ingNum) {
		this.ingNum = ingNum;
	}

	public int getBodNum() {
		return bodNum;
	}

	public void setBodNum(int bodNum) {
		this.bodNum = bodNum;
	}

	public String getBigName() {
		return bigName;
	}

	public void setBigName(String bigName) {
		this.bigName = bigName;
	}

	public String getBigAmount() {
		return bigAmount;
	}

	public void setBigAmount(String bigAmount) {
		this.bigAmount = bigAmount;
	}

}
