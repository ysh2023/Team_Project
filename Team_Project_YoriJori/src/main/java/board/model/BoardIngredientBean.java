package board.model;

public class BoardIngredientBean {
	private int big_num;
	private int ing_num;
	private int bod_num;
	private String big_name;
	private String big_category;

	public int getBig_num() {
		return big_num;
	}

	public void setBig_num(int big_num) {
		this.big_num = big_num;
	}

	public int getIng_num() {
		return ing_num;
	}

	public void setIng_num(int ing_num) {
		this.ing_num = ing_num;
	}

	public int getBod_num() {
		return bod_num;
	}

	public void setBod_num(int bod_num) {
		this.bod_num = bod_num;
	}

	public String getBig_name() {
		return big_name;
	}

	public void setBig_name(String big_name) {
		this.big_name = big_name;
	}

	public String getBig_category() {
		return big_category;
	}

	public void setBig_category(String big_category) {
		this.big_category = big_category;
	}

}
